# QBCore Blips Script

This is a simple script for FiveM using the QBCore framework that adds blips to the GTA V map. Blips are markers on the map that indicate locations of interest such as police stations, hospitals, and mechanic shops.

## Features

- Adds custom blips to the GTA V map.
- Configurable blip titles, colors, icons, and coordinates.

